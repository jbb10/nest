    @skip_without_docling
    def test_incremental_sync_skips_unchanged_file_and_makes_no_vision_calls(
        self, initialized_project: Path
    ) -> None:
        """AC: Second sync on an unchanged PDF skips processing and makes no LLM calls.

        Given the first sync has already produced image descriptions,
        when nest sync runs again without any source-file changes,
        then the file is skipped (checksum match)
        and the mock server receives no additional calls.
        """
        project_dir = initialized_project
        _put_image_fixture_in_project(project_dir)

        with MockVisionServer(response=CANNED_DESCRIPTION) as server:
            # First sync: converts PDF and describes images.
            first = run_cli(
                ["sync"], cwd=project_dir, timeout=300, env=server.env_vars()
            )
            assert first.exit_code == 0, (
                f"First sync failed:\nstdout: {first.stdout}\nstderr: {first.stderr}"
            )
            calls_after_first = server.call_count
            assert calls_after_first > 0, (
                "Expected at least one mock server call during the first sync"
            )

            # Second sync: source unchanged — file skipped, no vision calls.
            second = run_cli(
                ["sync"], cwd=project_dir, timeout=300, env=server.env_vars()
            )
            assert second.exit_code == 0, (
                f"Second sync failed:\nstdout: {second.stdout}\nstderr: {second.stderr}"
            )
            calls_after_second = server.call_count

        assert calls_after_second == calls_after_first, (
            f"Expected no additional server calls on second sync (unchanged file), "
            f"but call count increased from {calls_after_first} to {calls_after_second}"
        )

        # Descriptions from the first sync are preserved in the output.
        output_md = project_dir / "_nest_context" / "image_doc.md"
        assert output_md.exists()
        content = output_md.read_text()
        assert CANNED_DESCRIPTION in content, (
            "Expected canned description to be preserved after incremental sync"
        )
