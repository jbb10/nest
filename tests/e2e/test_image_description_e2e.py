"""E2E tests for image description via vision LLM (Story 7.5).

Tests verify the full image description pipeline using real Docling processing,
subprocess CLI invocation, and a mocked vision LLM HTTP server.  No real API
calls to external vision providers are made in any test.

Mocking strategy
----------------
``MockVisionServer`` starts a local HTTP server on a random port that returns a
canned OpenAI-compatible JSON response for every POST request.  Subprocesses are
launched with env vars (``NEST_AI_API_KEY``, ``NEST_AI_ENDPOINT``, …) pointing
at the mock server, so both text-enrichment and vision LLM calls are handled by
the same in-process stub.

The ``image_doc.pdf`` fixture (generated by
``tests/e2e/fixtures/generate_fixtures.py``) contains one embedded bar-chart
image.  Docling detects it as a ``PictureItem`` with ``label=bar_chart``.
"""

import json
import shutil
import threading
import time
from http.server import BaseHTTPRequestHandler, HTTPServer
from pathlib import Path

import pytest

from .conftest import run_cli, skip_without_docling

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

_FIXTURES_DIR = Path(__file__).parent / "fixtures"

#: Canned text description returned by the mock vision server.
CANNED_DESCRIPTION = "A bar chart showing quarterly sales performance for 2025."

#: Canned Mermaid diagram returned by the mock vision server.
CANNED_MERMAID = (
    "```mermaid\nflowchart TD\n    A[Start] --> B[Process] --> C[End]\n```"
)

_MOCK_PROMPT_TOKENS = 150
_MOCK_COMPLETION_TOKENS = 75

# ---------------------------------------------------------------------------
# Mock server
# ---------------------------------------------------------------------------


class _MockOpenAIHandler(BaseHTTPRequestHandler):
    """Minimal HTTP handler that returns canned OpenAI-style chat completions.

    Class-level attributes are mutated by :class:`MockVisionServer` to
    configure per-test responses and track invocation counts.
    """

    # Per-server-session configuration (set in MockVisionServer.__enter__)
    response_body: str = CANNED_DESCRIPTION
    #: Shared call counter — a one-element list so mutations are visible
    #: across the class hierarchy without needing instance state.
    call_counter: list[int] = [0]

    def do_POST(self) -> None:  # noqa: N802
        """Handle POST /v1/chat/completions with a canned response."""
        content_length = int(self.headers.get("Content-Length", 0))
        self.rfile.read(content_length)  # consume request body

        _MockOpenAIHandler.call_counter[0] += 1

        body = json.dumps(
            {
                "id": "chatcmpl-mock-e2e",
                "object": "chat.completion",
                "created": int(time.time()),
                "model": "mock-vision",
                "choices": [
                    {
                        "index": 0,
                        "message": {
                            "role": "assistant",
                            "content": _MockOpenAIHandler.response_body,
                        },
                        "finish_reason": "stop",
                        "logprobs": None,
                    }
                ],
                "usage": {
                    "prompt_tokens": _MOCK_PROMPT_TOKENS,
                    "completion_tokens": _MOCK_COMPLETION_TOKENS,
                    "total_tokens": _MOCK_PROMPT_TOKENS + _MOCK_COMPLETION_TOKENS,
                },
            }
        ).encode()

        self.send_response(200)
        self.send_header("Content-Type", "application/json")
        self.send_header("Content-Length", str(len(body)))
        self.end_headers()
        self.wfile.write(body)

    def log_message(self, format: str, *args: object) -> None:  # noqa: A002
        """Suppress default HTTP request logging."""


class MockVisionServer:
    """Context manager for a local mock OpenAI-compatible LLM server.

    Starts an :class:`~http.server.HTTPServer` on a random free port that
    accepts any POST request and replies with a canned OpenAI chat-completion
    JSON body.  Both text-enrichment and vision LLM calls from the nest
    subprocess are routed through this server.

    Usage::

        with MockVisionServer(response=CANNED_DESCRIPTION) as server:
            result = run_cli(["sync"], cwd=project_dir, env=server.env_vars())
        assert server.call_count > 0
    """

    def __init__(self, response: str = CANNED_DESCRIPTION) -> None:
        self._response = response
        self._server: HTTPServer | None = None
        self._thread: threading.Thread | None = None
        self.port: int = 0

    def __enter__(self) -> "MockVisionServer":
        # Reset shared call counter for this server session.
        _MockOpenAIHandler.call_counter[0] = 0

        # Build a per-invocation handler subclass so each MockVisionServer
        # context can use a different response body.
        handler_cls = type(
            "_Handler",
            (_MockOpenAIHandler,),
            {"response_body": self._response},
        )

        self._server = HTTPServer(("127.0.0.1", 0), handler_cls)
        self.port = self._server.server_address[1]
        self._thread = threading.Thread(
            target=self._server.serve_forever, daemon=True
        )
        self._thread.start()
        return self

    def __exit__(self, *_args: object) -> None:
        if self._server is not None:
            self._server.shutdown()

    @property
    def call_count(self) -> int:
        """Total API calls received by this server session."""
        return _MockOpenAIHandler.call_counter[0]

    def env_vars(self) -> dict[str, str]:
        """Env vars that route subprocess AI calls to this mock server.

        Both ``NEST_AI_*`` and ``OPENAI_*`` vars are set (with ``NEST_AI_*``
        taking priority in the nest fallback chain) so the subprocess uses the
        mock credentials regardless of the existing shell environment.
        """
        return {
            "NEST_AI_API_KEY": "mock-api-key-for-e2e-testing",
            "NEST_AI_ENDPOINT": f"http://127.0.0.1:{self.port}/v1",
            "NEST_AI_VISION_MODEL": "mock-vision-model",
            "NEST_AI_MODEL": "mock-text-model",
        }


