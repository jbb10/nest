@pytest.mark.e2e
class TestImageDescriptionE2E:
    """E2E tests for vision-based image description (Story 7.5).

    All tests use real Docling processing via subprocess CLI invocation.
    Vision LLM calls are intercepted by :class:`MockVisionServer`.
    """

    @skip_without_docling
    def test_image_description_produces_canned_description_in_output(
        self, initialized_project: Path
    ) -> None:
        """AC: Image description mode writes LLM text into the output markdown.

        Given a PDF with an embedded image and a mocked vision LLM,
        when nest sync runs,
        then the output markdown contains the canned description text
        and the sync summary includes 'Images described:'.
        """
        project_dir = initialized_project
        _put_image_fixture_in_project(project_dir)

        with MockVisionServer(response=CANNED_DESCRIPTION) as server:
            result = run_cli(
                ["sync"], cwd=project_dir, timeout=300, env=server.env_vars()
            )

        assert result.exit_code == 0, (
            f"Sync failed:\nstdout: {result.stdout}\nstderr: {result.stderr}"
        )

        output_md = project_dir / "_nest_context" / "image_doc.md"
        assert output_md.exists(), (
            "Expected '_nest_context/image_doc.md' to be created after sync"
        )
        content = output_md.read_text()
        assert CANNED_DESCRIPTION in content, (
            f"Expected canned description in output markdown.\n"
            f"stdout: {result.stdout}\n"
            f"Markdown (first 600 chars): {content[:600]}"
        )
        assert "Images described:" in result.stdout, (
            f"Expected 'Images described:' in sync summary.\nstdout: {result.stdout}"
        )

    @skip_without_docling
    def test_mermaid_response_appears_as_mermaid_block_in_output(
        self, initialized_project: Path
    ) -> None:
        """AC: Vision LLM mermaid response is preserved verbatim in markdown output.

        Given a mocked vision LLM that returns a fenced mermaid code block,
        when nest sync runs on a PDF with an image,
        then the output markdown contains a ```mermaid block with diagram elements.
        """
        project_dir = initialized_project
        _put_image_fixture_in_project(project_dir)

        with MockVisionServer(response=CANNED_MERMAID) as server:
            result = run_cli(
                ["sync"], cwd=project_dir, timeout=300, env=server.env_vars()
            )

        assert result.exit_code == 0, (
            f"Sync failed:\nstdout: {result.stdout}\nstderr: {result.stderr}"
        )

        output_md = project_dir / "_nest_context" / "image_doc.md"
        assert output_md.exists()
        content = output_md.read_text()
        assert "```mermaid" in content, (
            f"Expected mermaid code block in output.\n"
            f"Markdown (first 600 chars): {content[:600]}"
        )
        assert "flowchart TD" in content, (
            f"Expected mermaid diagram elements in output.\n"
            f"Markdown (first 600 chars): {content[:600]}"
        )

    @skip_without_docling
    def test_no_ai_env_vars_produces_image_placeholder(
        self, initialized_project: Path
    ) -> None:
        """AC: Without AI credentials, image output contains <!-- image --> placeholder.

        Given no AI API keys are configured,
        when nest sync runs on a PDF with an image,
        then the output markdown uses the Docling placeholder (<!-- image -->)
        and no description text is present.
        """
        project_dir = initialized_project
        _put_image_fixture_in_project(project_dir)

        # Override any ambient API keys with empty strings.
        # run_cli merges over os.environ, so empty-string values shadow real keys.
        # create_vision_provider() treats an empty string as "no key" (falsy).
        result = run_cli(
            ["sync"],
            cwd=project_dir,
            timeout=300,
            env={"NEST_AI_API_KEY": "", "OPENAI_API_KEY": ""},
        )

        assert result.exit_code == 0, (
            f"Sync failed:\nstdout: {result.stdout}\nstderr: {result.stderr}"
        )

        output_md = project_dir / "_nest_context" / "image_doc.md"
        assert output_md.exists()
        content = output_md.read_text()
        assert "<!-- image -->" in content, (
            f"Expected '<!-- image -->' placeholder in no-AI output.\n"
            f"Markdown (first 600 chars): {content[:600]}"
        )
        assert CANNED_DESCRIPTION not in content, (
            "Expected no description text in output without AI vision configured"
        )
        assert "Images described:" not in result.stdout, (
            f"Expected no 'Images described:' line without AI.\nstdout: {result.stdout}"
        )

    @skip_without_docling
    def test_no_ai_flag_produces_image_placeholder_and_no_vision_calls(
        self, initialized_project: Path
    ) -> None:
        """AC: --no-ai flag disables vision even when AI env vars are configured.

        Given AI env vars point at the mock server,
        when nest sync --no-ai runs,
        then the output uses <!-- image --> placeholders
        and zero calls are made to the mock (vision) server.
        """
        project_dir = initialized_project
        _put_image_fixture_in_project(project_dir)

        with MockVisionServer(response=CANNED_DESCRIPTION) as server:
            result = run_cli(
                ["sync", "--no-ai"],
                cwd=project_dir,
                timeout=300,
                env=server.env_vars(),
            )
            calls_with_no_ai_flag = server.call_count

        assert result.exit_code == 0, (
            f"Sync failed:\nstdout: {result.stdout}\nstderr: {result.stderr}"
        )

        output_md = project_dir / "_nest_context" / "image_doc.md"
        assert output_md.exists()
        content = output_md.read_text()
        assert "<!-- image -->" in content, (
            f"Expected '<!-- image -->' placeholder with --no-ai.\n"
            f"Markdown (first 600 chars): {content[:600]}"
        )
        assert CANNED_DESCRIPTION not in content, (
            "Expected no description text with --no-ai flag"
        )
        assert calls_with_no_ai_flag == 0, (
            f"Expected zero mock server calls with --no-ai, got {calls_with_no_ai_flag}"
        )

    @skip_without_docling
    def test_token_reporting_includes_vision_tokens(
        self, initialized_project: Path
    ) -> None:
        """AC: Sync summary 'AI tokens:' line aggregates vision description tokens.

        Given image descriptions are generated via the mocked vision LLM,
        when nest sync completes,
        then stdout includes an 'AI tokens:' line with non-zero token counts.
        """
        project_dir = initialized_project
