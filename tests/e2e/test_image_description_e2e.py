def _put_image_fixture_in_project(project_dir: Path) -> Path:
    """Copy ``image_doc.pdf`` fixture into ``_nest_sources/`` of the project."""
    dest = project_dir / "_nest_sources" / "image_doc.pdf"
    shutil.copy(_FIXTURES_DIR / "image_doc.pdf", dest)
    return dest


